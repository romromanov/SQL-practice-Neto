/*Задание №1
Выведите уникальные названия городов из таблицы городов. В результирующей таблице должны быть следующие столбцы: название города.
*/
select distinct city
from city c 

/*Задание №2
Доработайте запрос из предыдущего задания, чтобы запрос выводил только те города, названия которых начинаются на “L” и заканчиваются на “a”, и названия не содержат пробелов.
Ограничение: использование регулярных выражений запрещено.
В результирующей таблице должны быть следующие столбцы: название города.
*/
select distinct city
from city c
where city like 'L%a' and city not like '% %'

/*Задание №3
Выведите информацию о 10-ти последних платежах за прокат фильмов.
В результирующей таблице должны быть следующие столбцы: все столбцы из таблицы.
